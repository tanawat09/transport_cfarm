    private function money(string $value): float
    {
        return (float) str_replace(',', '', $value);
    }

    private function date(string $value): string
    {
        $value = trim($value);
        $thaiMonths = [
            'ม.ค.' => 1,
            'ก.พ.' => 2,
            'มี.ค.' => 3,
            'เม.ย.' => 4,
            'พ.ค.' => 5,
            'มิ.ย.' => 6,
            'ก.ค.' => 7,
            'ส.ค.' => 8,
            'ก.ย.' => 9,
            'ต.ค.' => 10,
            'พ.ย.' => 11,
            'ธ.ค.' => 12,
        ];

        if (preg_match('/^(\d{1,2})\s+(\S+)\s+(\d{2,4})$/u', $value, $matches)) {
            return Carbon::create(
                $this->year((int) $matches[3]),
                $thaiMonths[$matches[2]],
                (int) $matches[1]
            )->toDateString();
        }

        if (preg_match('/^(\d{1,2})-(\d{1,2})-(\d{2}|\d{4})$/', $value, $matches)) {
            return Carbon::create(
                $this->year((int) $matches[3]),
                (int) $matches[2],
                (int) $matches[1]
            )->toDateString();
        }

        return Carbon::parse($value)->toDateString();
    }

    private function year(int $year): int
    {
        if ($year < 100) {
            return 2000 + $year;
        }

        return $year > 2400 ? $year - 543 : $year;
    }
